<?php
// Déclaration de la fonction avec deux paramètres
function additionner($a, $b) {
    // Retourne la somme des deux paramètres
    return $a + $b;
}

// Appel de la fonction avec des arguments
$resultat = additionner(5, 10);
echo "La somme de 5 et 10 est : " . $resultat . "<br>";
?>


let number = parseFloat(window.prompt("Entrez un numéro entre 1 et 7 "));


if (number === 1) {
	window.alert("Lundi");
} else if (number === 2) {
	window.alert("Mardi"); 
} else if (number === 3) {
	window.alert("Mercredi");
} else if (number === 4) {
	window.alert("Jeudi");
} else if (number === 5) {
	window.alert("Vendredi");
} else if (number === 6) {
	window.alert("Samedi");
} else if (number === 7) {
	window.alert("Dimanche");
} else {
	window.alert("Entrée invalide. Veuillez entrer un nombre entre 1 et 7.");
}